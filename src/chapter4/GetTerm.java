public class GetTerm {
    public static void main(String[] args){
        int x=20;
        if(x>30){
            System.out.println("x>30");
        }
        else if(x>20){
            System.out.println("x>20");
        }
        else if(x>10){
            System.out.println("x>10");
        }
        else if(x>0){
            System.out.println("x>0");
        }
    }
}
